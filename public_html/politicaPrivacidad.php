            <td>
                <p>Los datos personales de los usuarios serán utilizados únicamente para el cumplimiento de los fines indicados en el (los) formulario (s) correspondiente (s) y siempre dentro de las competencias y atribuciones de Operamaq Empresa SpA. En ellos se informará sobre las específicas finalidades para las que se recogen los datos. Si en algún caso llegara a requerirse un tratamiento distinto de lo indicado deberá quedar registro del consentimiento del titular.</p>
                <p>Los datos personales que los titulares revelen en sistemas abiertos al público, como redes sociales o foros, asociados al sitio de Operamaq Empresa Spa, serán almacenados junto al resto de contenidos, con el fin de permitir el funcionamiento, mantenimiento y transparencia de dichos sistemas y las contribuciones en él realizadas. En ningún caso estos datos serán tratados por la Empresa con fines diferentes, sin solicitar el previo consentimiento de su titular.</p>
                <p>Los datos personales recogidos a través de www.operamaq.cl serán almacenados únicamente durante el tiempo necesario para garantizar la prestación del servicio o el cumplimiento de la finalidad para la que fueron recabados.</p>
                <p><b>Utilización con fines estadísticos:</b> Los datos recogidos a través de la página web podrán ser conservados y tratados con fines estadísticos, siempre y cuando sea imposible la identificación de sus titulares.</p>
